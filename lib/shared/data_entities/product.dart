class Product
{
  final String id;
  final String nombre;
  final String foto;
  final String alergeno;
  final double precio;
  final String tienda;
  
  Product({
    required this.id,
    required this.nombre,
    required this.foto,
    required this.alergeno,
    required this.precio,
    required this.tienda,
  });
}